// button
let button = document.getElementById("button");
let icon = document.getElementById("btn-curve");
let btn = document.getElementById("btn");


button.addEventListener("mouseenter",()=>{
    
    icon.style.transform = "rotateZ(0deg)";
    icon.style.backgroundColor = "rgb(8, 2, 39)";
    icon.style.width = "15rem";
    btn.style.color = "rgb(255, 255, 255)";
    
})
button.addEventListener("mouseleave",()=>{
    
    icon.style.transform = "rotateZ(15deg)";
    icon.style.backgroundColor = "rgba(152, 43, 3, 0.714)";
    icon.style.width = "1rem";
    btn.style.color = "rgb(0, 0, 0)";

})

